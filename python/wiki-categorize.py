import requests
import time
import csv
import json
from collections import defaultdict
from urllib.parse import unquote, urlparse

WIKI_API_URL = "https://en.wikipedia.org/w/api.php"

def get_wikipedia_categories(page_title):
    """Fetch categories for a Wikipedia page."""
    params = {
        "action": "query",
        "format": "json",
        "prop": "categories",
        "titles": page_title,
        "cllimit": "max"
    }
    
    response = requests.get(WIKI_API_URL, params=params)
    data = response.json()

    pages = data.get("query", {}).get("pages", {})
    
    for page in pages.values():
        if "categories" in page:
            return [cat["title"].replace("Category:", "") for cat in page["categories"]]

    return []

def extract_page_title(url):
    """Extract the Wikipedia page title from a URL."""
    parsed_url = urlparse(url)
    if "wikipedia.org" in parsed_url.netloc:
        return unquote(parsed_url.path.split("/")[-1])
    return None

def categorize_wikipedia_pages(urls):
    """Group Wikipedia URLs by their categories."""
    category_groups = defaultdict(list)
    
    for url in urls:
        title = extract_page_title(url)
        if title:
            categories = get_wikipedia_categories(title)
            
            for category in categories:
                category_groups[category].append(url)
            
            time.sleep(1)  # Prevent API rate limits
    
    return category_groups

def save_to_txt(category_groups, filename="categorized_urls.txt"):
    """Save results to a human-readable text file."""
    with open(filename, "w", encoding="utf-8") as file:
        for category, pages in category_groups.items():
            file.write(f"== {category} ==\n")
            for page in pages:
                file.write(f"  - {page}\n")
            file.write("\n")

def save_to_csv(category_groups, filename="categorized_urls.csv"):
    """Save results to a CSV file."""
    with open(filename, "w", newline="", encoding="utf-8") as file:
        writer = csv.writer(file)
        writer.writerow(["Category", "Wikipedia URL"])
        
        for category, pages in category_groups.items():
            for page in pages:
                writer.writerow([category, page])

def save_to_json(category_groups, filename="categorized_urls.json"):
    """Save results to a JSON file."""
    with open(filename, "w", encoding="utf-8") as file:
        json.dump(category_groups, file, indent=4, ensure_ascii=False)

